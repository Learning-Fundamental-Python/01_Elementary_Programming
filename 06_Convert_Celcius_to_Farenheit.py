# Write a simple program to convert degree of Celcius to Farenheit

# Prompt user for input degree in Celcius
celcius = int(input('Enter a degree in Celcius : '))

# Convert Celcius to Farenheit
fahrenheit = (9/5) * celcius + 32

# Display the result
print(f"{celcius} Celcius is {fahrenheit} Fahrenheit")
